//12. Zip
class Zip{
	String material;
    double length;
    String color;
    String type;
    boolean isWaterproof;
    boolean hasAutoLock;
    String application;
    boolean isRustProof;
    String brand;
    boolean hasDualSlider;
    String closureType;
    boolean isReversible;
    double width;
    boolean isCustomizable;
    String durability;
    boolean isHeatResistant;
    int warrantyYears;
    double price;
    boolean hasHiddenTeeth;
    public Zip(String material, double length, String color, String type, boolean isWaterproof, boolean hasAutoLock,
               String application, boolean isRustProof, String brand, boolean hasDualSlider, String closureType,
               boolean isReversible, double width, boolean isCustomizable, String durability, boolean isHeatResistant,
               int warrantyYears, double price, boolean hasHiddenTeeth) {
        this.material = material;
        this.length = length;
        this.color = color;
        this.type = type;
        this.isWaterproof = isWaterproof;
        this.hasAutoLock = hasAutoLock;
        this.application = application;
        this.isRustProof = isRustProof;
        this.brand = brand;
        this.hasDualSlider = hasDualSlider;
        this.closureType = closureType;
        this.isReversible = isReversible;
        this.width = width;
        this.isCustomizable = isCustomizable;
        this.durability = durability;
        this.isHeatResistant = isHeatResistant;
        this.warrantyYears = warrantyYears;
        this.price = price;
        this.hasHiddenTeeth = hasHiddenTeeth;
    }
}