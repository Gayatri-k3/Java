public class Array{
	public static void main(String args[]){
	String[] a1 = {"one","two", "three", "Four"};
	double[] a2 = {1.11,2.22,3.33,4.44, 5.55};
	char[] a3 = {'o','t','g'};
	int[] a4 = {11,22,33,44,55,66};
	System.out.println("The length of String array is :" +a1.length);
	System.out.println("The length of double array is :" +a2.length);
	System.out.println("The length of char array is :" +a3.length);
	System.out.println("The length of int array is :" +a4.length);
	}
}